n = int(input("Enter a number:"))


for i in range(n // 2):
    print('*' * (n // 2 - i) + ' ' * (2 * i) + '*' * (n // 2 - i))
for i in range(n // 2, n):
    print('*' * (i - n // 2 + 1) + ' ' * (n - 2 * (i - n // 2 + 1)) + '*' * (i - n // 2 + 1))
